"""Merge missing catalog entries into datasets.json"""

import json
import re

with open("datasets.json", "r", encoding="utf-8") as f:
    data = json.load(f)

print(f"Starting entries: {len(data)}")

# Build lookup sets for dedup
existing_names = set(d["name"].lower().strip() for d in data)
existing_urls = set()
for d in data:
    url = d.get("url", "").lower().strip().rstrip("/")
    if url:
        existing_urls.add(url)
        for prefix in ["https://www.", "http://www.", "https://", "http://"]:
            if url.startswith(prefix):
                existing_urls.add(url[len(prefix):])

existing_ids = set(d["id"] for d in data)


def slug(name):
    s = name.lower()
    s = re.sub(r"[^a-z0-9\s-]", "", s)
    s = re.sub(r"\s+", "-", s.strip())
    s = re.sub(r"-+", "-", s)
    return s


def url_exists(url):
    if not url:
        return False
    u = url.lower().strip().rstrip("/")
    if u in existing_urls:
        return True
    for prefix in ["https://www.", "http://www.", "https://", "http://"]:
        if u.startswith(prefix):
            if u[len(prefix):] in existing_urls:
                return True
    return False


def name_exists(name):
    return name.lower().strip() in existing_names


def name_exists_partial(substr):
    for n in existing_names:
        if substr in n:
            return True
    return False


def should_skip(name, url):
    if name_exists(name):
        return True
    if url and url_exists(url):
        return True
    # Multi-domain platforms already present
    skip_keywords = [
        "copernicus data space", "nasa earthdata", "google earth engine",
        "sentinel hub", "overture maps",
    ]
    nl = name.lower()
    for sn in skip_keywords:
        if sn in nl and name_exists_partial(sn):
            return True
    return False


def unique_id(s):
    base = slug(s)
    if base not in existing_ids:
        return base
    i = 2
    while f"{base}-{i}" in existing_ids:
        i += 1
    return f"{base}-{i}"


def add_entry(e, new_entries):
    if should_skip(e["name"], e["url"]):
        print(f"  SKIP: {e['name']}")
        return
    eid = unique_id(e["name"])
    e["id"] = eid
    existing_ids.add(eid)
    existing_names.add(e["name"].lower().strip())
    if e["url"]:
        existing_urls.add(e["url"].lower().strip().rstrip("/"))
    new_entries.append(e)
    print(f"  ADD:  {e['name']} -> {eid}")


